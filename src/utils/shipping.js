// ── Book order storage ─────────────────────────────────────
const ORDERS_KEY = 'book_orders';

export const getBookOrders = () =>
  JSON.parse(localStorage.getItem(ORDERS_KEY) || '[]');

export const saveBookOrder = (data) => {
  const orders = getBookOrders();
  const order = {
    id:       'ORD-' + Date.now(),
    ...data,
    status:   'pending',
    orderedAt: new Date().toISOString(),
  };
  localStorage.setItem(ORDERS_KEY, JSON.stringify([...orders, order]));
  return order;
};
