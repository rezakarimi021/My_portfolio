/**
 * @param {string} provinceName - Persian province name
 * @param {number} weightGrams  - package weight in grams
 * @param {'standard'|'express'} method
 * @returns {{ cost: number, zone: number, days: string }}
 */
export const calcShipping = (provinceName, weightGrams, method = 'standard') => {
  const province = PROVINCES.find(p => p.name === provinceName);
  const zone = province?.zone ?? 3;

  const base  = BASE[method][zone];
  const extra = EXTRA[method][zone];
  const overKg = Math.max(0, weightGrams - 1000);
  const surcharge = Math.ceil(overKg / 500) * extra;

  return {
    cost: base + surcharge,
    zone,
    days: DELIVERY_DAYS[method][zone],
    label: ZONE_LABELS[zone],
    distance: ZONE_DISTANCES[zone],
  };
};

